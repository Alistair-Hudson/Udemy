public class IfThen {
    
    public static void main(String[] args) {
        
        boolean isAlien = false;
        int a = 2;

        if (isAlien == false){
            System.out.println("Not an Alien");
        }
        else if (a != 2){
            System.out.println("a is not 2");
        }
        else{
            System.out.println("you have reached an else");
        }
    }
}
